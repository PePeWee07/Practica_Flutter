class User {
  final String id;
  final String firstName;
  final String lastName;
  final String email;
  final String phone;
  final String pictureUrl;
  final String city;
  final String country;

  const User({
    required this.id,
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.phone,
    required this.pictureUrl,
    required this.city,
    required this.country,
  });
}
